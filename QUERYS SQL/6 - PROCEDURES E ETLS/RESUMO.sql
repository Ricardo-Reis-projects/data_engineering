 
 USE TESTES

 CREATE TABLE RESUMO 
	(
	ORG				VARCHAR(3),
	LOGO			VARCHAR(3),
	CONTA			VARCHAR(19),
	ST_CONTA		VARCHAR(1),
	CONTA_2			VARCHAR(1),
	DT_B1_CONTA		VARCHAR(8),
	B2_CONTA		VARCHAR(1),
	DT_B2_CONTA		VARCHAR(8),
	DT_ULT_CMPR		VARCHAR(8),
