		SUBSTRING(DADOS, 4, 3)		AS LOGO, 
		SUBSTRING(DADOS, 7, 19)		AS CONTA,
		SUBSTRING(DADOS, 26, 1)		AS ST_CONTA,
		SUBSTRING(DADOS, 27, 1)		AS CONTA_2,
		SUBSTRING(DADOS, 28, 8)		AS DT_B1_CONTA,
		SUBSTRING(DADOS, 36, 1)		AS B2_CONTA,
